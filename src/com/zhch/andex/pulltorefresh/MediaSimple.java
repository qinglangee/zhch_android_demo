package com.zhch.andex.pulltorefresh;

public class MediaSimple {
	private Long dashboard_id;
	private String title;
	private String text;
	private String image;
	private Integer notes_num;
	private Integer width;
	private Integer height;
	private Long media_id;
	private Integer editor_num;
	private String link;
	
	public Long getDashboard_id() {
		return dashboard_id;
	}
	public void setDashboard_id(Long dashboard_id) {
		this.dashboard_id = dashboard_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public Integer getNotes_num() {
		return notes_num;
	}
	public void setNotes_num(Integer notes_num) {
		this.notes_num = notes_num;
	}
	public Integer getWidth() {
		return width;
	}
	public void setWidth(Integer width) {
		this.width = width;
	}
	public Integer getHeight() {
		return height;
	}
	public void setHeight(Integer height) {
		this.height = height;
	}
	public Long getMedia_id() {
		return media_id;
	}
	public void setMedia_id(Long media_id) {
		this.media_id = media_id;
	}
	public Integer getEditor_num() {
		return editor_num;
	}
	public void setEditor_num(Integer editor_num) {
		this.editor_num = editor_num;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
}

